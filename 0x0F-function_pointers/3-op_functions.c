#include "calc.h"
/**
 * op_add-adds two int
 * @a:first int
 * @b:second int
 * Return:sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- subracts a from b
 * @a:first int
 * @b:second int
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multplies two int
 * @a:first int
 * @b:second int
 * Return:product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div-divides a by b
 * @a:first int
 * @b:second int
 * Return:quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- finds the remainder
 * @a:first int
 * @b:second int
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
